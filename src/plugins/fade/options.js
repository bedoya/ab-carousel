/**
 * @function default_options
 * Set options that can be overridden
 * when creating the ABCarousel instance.
 */
const default_options = () => {
    return {
        transition_speed: 500,
        slide_class: 'ab-carousel-slide',
        max_opacity: 1,
        min_opacity: 0,
    };
};

export default default_options;

